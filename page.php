Bagi sebagian orang, hunian yang berkiblat pada konsep arsitektur Eropa menjadi pilihan favorit. Tak ketinggalan, dekorasi di beberapa sudut bangunan menghiasi rumah bergaya Eropa karena memiliki luas lahan yang memadai dan bekarakter kokoh, megah,dan modern.

Tertarik dengan hunian bergaya tersebut yang berlokasi di area strategis di Pekanbaru? Anda dapat menjatuhkan pilihan pada perumahan Cluster The Benison Residence, hunian terbaru di kota Pekanbaru. PT. Mitrakurnia Mitraabadi (PT.BKMA) telah menyiapkan investasi untuk mengembangkan cluster tersebut.
<br/>
The Benison Residence mengusung konsep "Eco Living" dan dibangun di areal seluas 4 Ha. Atmosfir yang diinginkan itu dapat terwujud di The Benison Residence ini karena terletak dikawasan yang sedang berkembang di daerah kota Pekanbaru.
<br/>
Kelebihan lain dari Perumahan Cluster The Benison Residence ini adalah adanya ruang terbuka hijau (RTH) yang cukup luas dan instalasi listriknya dibawah tanah (Underground) sehingga view lingkunganya tertata rapi. Dari total 4 Ha yang dibangun terdapat sekitar 60 persen untuk ruang terbuka hijau (RTH), infrastruktur dan fasilitas pendukung.
